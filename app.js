function setupLightbox(project) {
  const lightbox = document.querySelector("[data-lightbox]");
  if (!lightbox) return;
  const image = lightbox.querySelector("[data-lightbox-image]");
  const caption = lightbox.querySelector("[data-lightbox-caption]");
  const count = lightbox.querySelector("[data-lightbox-count]");
  let activeIndex = 0;
  const show = (index) => {
    activeIndex = (index + project.gallery.length) % project.gallery.length;
    const galleryImage = project.gallery[activeIndex];
    image.src = asset(galleryImage);
    image.alt = project.title;
    caption.textContent = galleryCaption(galleryImage, project);
    count.textContent = `${activeIndex + 1}/${project.gallery.length}`;
  };
  document.querySelectorAll("[data-gallery-index]").forEach((button) => {
    button.addEventListener("click", () => {
      show(Number(button.dataset.galleryIndex));
      lightbox.hidden = false;
      document.body.classList.add("lightbox-open");
    });
  });
  const close = () => {
    lightbox.hidden = true;
    document.body.classList.remove("lightbox-open");
  };
  lightbox.querySelector("[data-lightbox-close]").addEventListener("click", close);
  lightbox.querySelector("[data-lightbox-prev]").addEventListener("click", () => show(activeIndex - 1));
  lightbox.querySelector("[data-lightbox-next]").addEventListener("click", () => show(activeIndex + 1));
  lightbox.addEventListener("click", (event) => {
    if (event.target === lightbox) close();
  });
  document.addEventListener("keydown", (event) => {
    if (event.key === "Escape" && !lightbox.hidden) close();
    if (event.key === "ArrowLeft" && !lightbox.hidden) show(activeIndex - 1);
    if (event.key === "ArrowRight" && !lightbox.hidden) show(activeIndex + 1);
  });
}

function renderDynamic() {
  renderProjectGrids();
  renderCarousel();
  renderHeroCarousel();
  renderProjectDetail();
  reveal();
}

function reveal() {
  const items = document.querySelectorAll(".reveal");
  const observer = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) entry.target.classList.add("is-visible");
    });
  }, { threshold: 0.12 });
  items.forEach((item) => observer.observe(item));
}

function init() {
  setTheme(localStorage.getItem("rubic-theme") || "light");
  setLanguage(localStorage.getItem("rubic-lang") || "en");
  document.querySelectorAll("[data-lang-select]").forEach((select) => {
    select.addEventListener("change", () => setLanguage(select.value));
  });
  document.querySelector("[data-theme-toggle]")?.addEventListener("click", () => {
    setTheme(document.documentElement.dataset.theme === "dark" ? "light" : "dark");
  });
  document.querySelector("[data-menu]")?.addEventListener("click", (event) => {
    const nav = document.querySelector(".nav");
    nav?.classList.toggle("is-open");
    event.currentTarget.setAttribute("aria-expanded", String(nav?.classList.contains("is-open")));
  });
}

document.addEventListener("DOMContentLoaded", init);

