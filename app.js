let carouselTimer;
let carouselIndex = 0;
let heroTimer;
let heroIndex = 0;

function currentLang() {
  return document.documentElement.lang || "en";
}

function t(key) {
  return I18N[currentLang()]?.[key] || I18N.en[key] || key;
}

function projectText(project, key) {
  return project[currentLang()]?.[key] || project.en[key] || "";
}

function localizedValue(value) {
  return currentLang() === "de" ? (deValueMap[value] || value) : value;
}

function galleryCaption(image, project) {
  const captions = galleryCaptionMap[currentLang()] || galleryCaptionMap.en;
  const key = Object.keys(captions).find((item) => image.includes(item));
  if (key) return captions[key];
  return currentLang() === "de"
    ? `${project.title}: Projektbild aus der Galerie.`
    : `${project.title}: image from the project gallery.`;
}

function asset(path) {
  return encodeURI(path);
}

function setLanguage(lang) {
  document.documentElement.lang = lang;
  localStorage.setItem("rubic-lang", lang);
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    el.textContent = t(el.dataset.i18n);
  });
  document.querySelectorAll("[data-lang-select]").forEach((select) => {
    select.value = lang;
  });
  renderDynamic();
}

function setTheme(theme) {
  document.documentElement.dataset.theme = theme;
  localStorage.setItem("rubic-theme", theme);
  const button = document.querySelector("[data-theme-toggle]");
  if (button) button.textContent = theme === "dark" ? "Light" : "Dark";
}

function projectCard(project, large = false) {
  return `
    <a class="project-card reveal ${large ? "featured-wide" : ""}" href="project.html?id=${project.id}">
      <img src="${asset(project.hero)}" alt="${project.title}">
      <div class="project-meta">
        <small>${project.city} / ${localizedValue(project.use)}</small>
        <h3>${project.title}</h3>
        <p>${localizedValue(project.status)}</p>
      </div>
    </a>
  `;
}

function renderProjectGrids() {
  document.querySelectorAll("[data-project-grid]").forEach((grid) => {
    const limit = Number(grid.dataset.limit || projects.length);
    grid.innerHTML = projects.slice(0, limit).map((project, index) => projectCard(project, index === 0 && limit <= 4)).join("");
  });
}

function renderCarousel() {
  const root = document.querySelector("[data-project-carousel]");
  if (!root) return;
  clearInterval(carouselTimer);
  root.innerHTML = `
    <a class="carousel-frame" data-carousel-link href="#">
      <img data-carousel-image alt="">
      <div class="carousel-copy">
        <p class="kicker" data-carousel-kicker></p>
        <h3 data-carousel-title></h3>
        <p data-carousel-text></p>
      </div>
    </a>
    <div class="carousel-controls">
      ${projects.map((project, index) => `<button type="button" data-carousel-dot="${index}" aria-label="${project.title}"></button>`).join("")}
    </div>
  `;
  const show = (index) => {
    carouselIndex = (index + projects.length) % projects.length;
    const project = projects[carouselIndex];
    root.querySelector("[data-carousel-link]").href = `project.html?id=${project.id}`;
    root.querySelector("[data-carousel-image]").src = asset(project.hero);
    root.querySelector("[data-carousel-image]").alt = project.title;
    root.querySelector("[data-carousel-kicker]").textContent = `${project.city} / ${localizedValue(project.status)}`;
    root.querySelector("[data-carousel-title]").textContent = project.title;
    root.querySelector("[data-carousel-text]").textContent = projectText(project, "overview");
    root.querySelectorAll("[data-carousel-dot]").forEach((dot) => {
      dot.setAttribute("aria-current", String(Number(dot.dataset.carouselDot) === carouselIndex));
    });
  };
  root.querySelectorAll("[data-carousel-dot]").forEach((dot) => {
    dot.addEventListener("click", () => {
      show(Number(dot.dataset.carouselDot));
      clearInterval(carouselTimer);
      carouselTimer = setInterval(() => show(carouselIndex + 1), 5200);
    });
  });
  show(carouselIndex);
  carouselTimer = setInterval(() => show(carouselIndex + 1), 5200);
}

