function localizedValue(value) {
  return currentLang() === "de" ? (deValueMap[value] || value) : value;
}

function galleryCaption(image, project) {
  const captions = galleryCaptionMap[currentLang()] || galleryCaptionMap.en;
  const key = Object.keys(captions).find((item) => image.includes(item));
  if (key) return captions[key];
  return currentLang() === "de"
    ? `${project.title}: Projektbild aus der Galerie.`
    : `${project.title}: image from the project gallery.`;
}

function asset(path) {
  return encodeURI(path);
}

function absoluteUrl(path = "") {
  const cleanPath = String(path || "").replace(/^\/+/, "");
  return new URL(encodeURI(cleanPath), SITE.url).href;
}

function pageName() {
  const name = location.pathname.split("/").pop() || "index.html";
  return name || "index.html";
}

function setMeta(name, content, attr = "name") {
  if (!content) return;
  let tag = document.head.querySelector(`meta[${attr}="${name}"]`);
  if (!tag) {
    tag = document.createElement("meta");
    tag.setAttribute(attr, name);
    document.head.appendChild(tag);
  }
  tag.setAttribute("content", content);
}

function setLink(rel, href, extra = {}) {
  if (!href) return;
  let selector = `link[rel="${rel}"]`;
  if (extra.hreflang) selector += `[hreflang="${extra.hreflang}"]`;
  let tag = document.head.querySelector(selector);
  if (!tag) {
    tag = document.createElement("link");
    tag.setAttribute("rel", rel);
    if (extra.hreflang) tag.setAttribute("hreflang", extra.hreflang);
    document.head.appendChild(tag);
  }
  tag.setAttribute("href", href);
  Object.entries(extra).forEach(([key, value]) => {
    if (key !== "hreflang") tag.setAttribute(key, value);
  });
}

function setJsonLd(id, data) {
  let tag = document.getElementById(id);
  if (!tag) {
    tag = document.createElement("script");
    tag.type = "application/ld+json";
    tag.id = id;
    document.head.appendChild(tag);
  }
  tag.textContent = JSON.stringify(data);
}

